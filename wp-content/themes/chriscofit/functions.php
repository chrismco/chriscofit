function addlightboxrel_replace($string) {
	$pattern = '/<a(.*?)href="(.*?).(bmp|gif|jpeg|jpg|png)"(.*?)>/i';
  	$replacement = '<a$1href="$2.$3" rel=\'Lightbox[imagegroup]\'$4>';
	return preg_replace($pattern, $replacement, $string);
}

